#Aspas simples
print ('Matheus Peluso')

#Aspas Duplas
print ("Matheus Peluso")

#Escape
print ("Matheus \"Peluso\" ") # usando isso você consegue usar aspas dentro de uma string e a contra barra não aparece 

#r
print (r"Matheus \"Peluso\" ") # usando esse ele mostra tambem a barra invertida

# agora para não fazer essas coisas feias e sujar o seu codigo troque o tipo das aspas desta forma abaixo
print ("Matheus 'Peluso'")
print ('Matheus "Peluso"')
